function glmb_display_info(k,cdn_mean,cdn_mode,cdn_var,num_comp_post,num_trk_post,num_comp_trunc,num_trk_trunc)
  disp( [ 'K = ', int2str(k), ...
          ' | MEAN(card) = ' num2str(cdn_mean) ...
          ' | MODE(card) = ' int2str(cdn_mode) ...
          ' | VAR(card) = ' num2str(cdn_var,4) ...
          ' | NCOMP(post) = ' int2str(num_comp_post) ...
          ' | NTRK(post) = ' int2str(num_trk_post) ...
          ' | NCOMP(trunc) = ' int2str(num_comp_trunc) ...
          ' | NTRK(trunc) = ' int2str(num_trk_trunc) ] );
end
